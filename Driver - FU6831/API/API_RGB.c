/**
 * 初始化RGB端口
 *
 * @Writer  Any
 * @Version V1.1
 * @Date    2017-04-02
 *
 */
void Init_RGB(void)
{
    R_OE |= R_PinNum;
    G_OE |= G_PinNum;
    B_OE |= B_PinNum;

    Write_RGB(R_Pin, 1);
    Write_RGB(G_Pin, 1);
    Write_RGB(B_Pin, 1);
}
