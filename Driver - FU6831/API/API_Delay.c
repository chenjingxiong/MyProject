/**
 * 软件延时：ms
 *
 * @Writer  Any
 * @Version V1.0
 * @Date    2017-04-05
 *
 * @param   k          毫秒数
 */
void ms_Delay(unsigned short k)
{
    unsigned short i, j;

    for (i=0; i!=k; i++)
    {
        for (j=0; j!=2800; j++);
    }
}
